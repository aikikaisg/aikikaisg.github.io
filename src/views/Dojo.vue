    <h4>Dojo etiquette</h4>
    <ol>
        <li>When entering or leaving the dojo, it is proper to bow in the direction of O Sensei's picture, the kamiza, or the front of the dojo. You should also bow when entering or leaving the mat. It is also proper to bow to the instructor or Sensei of the class.</li>
        <li>Strictly no footwear on the mat. Please place all footwear neatly at the designated area in the dojo</li>
        <li>Be on time for class (10 - 15 minutes earlier is advised). In dojos where there is a need to prepare or arrange the mats before the training session, all students are urged to arrive early (20 minutes before class) to assist in the preparation process. Similarly, at the end of the training session, all students are to assist in the removal and storage of the mats. The assembly and removal of mats require the cooperative efforts of all students.</li>
        <li>If for whatever reasons you do happen to arrive late, sit quietly in seiza on the edge of the mat until the instructor grants permission to join practice.</li>
        <li>If you should have to leave the mat or dojo for any reason during class, approach the instructor and ask permission.</li>
        <li>Avoid sitting on the mat with your back to the picture of O Sensei or the kamiza. Also, do not lean against the walls or sit with your legs stretched out. (Either sit in seiza or cross-legged.)</li>
        <li>For safety reasons, remove watches, rings and other jewelry before practice. Avoid wearing makeup for lessons. You don't want to stain your or your fellow students' gi during practice.</li>
        <li>Do not bring food, gum, or beverages with you into the dojo. Do not consume any food or drinks during lessons.</li>
        <li>Please keep your finger and toe nails short.</li>
        <li>Please keep talking during class to a minimum. What conversation there is should be restricted to one topic -- Aikido.</li>
        <li>Carry out the directives of the instructor PROMPTLY. Do not keep the rest of the class waiting for you!</li>
        <li>Do not engage in rough-housing or needless contests of strength during class.</li>
        <li>Keep your gi clean, in good shape, and free of offensive odors.</li>
