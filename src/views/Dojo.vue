        <li>Remember that you are here to learn, and not to gratify your ego. An attitude of receptivity and humility (though not obsequiousness) is therefore advised.</li>
        <li>Preserve common-sense standards of decency and respect at all times. In terms of etiquette, when in doubt, always be thoughtful, courteous, and polite.</li>
